CREATE TABLE CAR (
	CAR_MODEL CHAR(20),
	MANUFACTURE_DATE DATE,
    STORAGE_DATE DATE,
	constraint CAR_PK PRIMARY KEY (CAR_MODEL, MANUFACTURE_DATE) ENABLE
);

CREATE TABLE CUSTOMER (
    ID NUMBER,
    LAST_NAME CHAR(20),
	FIRST_NAME CHAR(10),
    COMPANY CHAR(20),
	constraint CUSTOMER_PK PRIMARY KEY (ID) ENABLE
);

CREATE TABLE PURCHASE (
    ID NUMBER,
    ORDER_DATE DATE,
    CUSTOMER_ID NUMBER,
    MANAGER_ID NUMBER,
    CAR_MODEL CHAR(20),
    MANUFACTURE_DATE DATE,
    QUANTITY NUMBER,
	constraint PURCHASE_PK PRIMARY KEY (ID) ENABLE
);

CREATE TABLE MANAGER (
    ID NUMBER,
	LAST_NAME CHAR(20),
	EXPERIENCE NUMBER,
    RATING NUMBER,
	constraint MANAGER_PK PRIMARY KEY (ID) ENABLE
);

ALTER TABLE PURCHASE ADD CONSTRAINT "PURCHASE_FK0" FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(ID);
ALTER TABLE PURCHASE ADD CONSTRAINT "PURCHASE_FK1" FOREIGN KEY (MANAGER_ID) REFERENCES MANAGER(ID);
ALTER TABLE PURCHASE ADD CONSTRAINT "PURCHASE_FK2" FOREIGN KEY (CAR_MODEL, MANUFACTURE_DATE) REFERENCES CAR(CAR_MODEL, MANUFACTURE_DATE);

